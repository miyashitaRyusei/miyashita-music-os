/**
 * 楽曲分析データを元に、LLM（Gemini等）へ入力するためのプロンプトを生成する
 */
export function generateAIPrompt({ radarData, pitchPatterns, rhythmPatterns }) {
  // マクロデータのテキスト化（8軸）
  const macroText = radarData
    .map((d) => `- ${d.axis}: 自作曲(Original)=${d.original}, 理想(Like)=${d.like}`)
    .join('\n');

  // ミクロデータのテキスト化（代表的なパターンを抜粋）
  const microPitch = pitchPatterns
    .filter(p => p.source === 'original')
    .slice(0, 5)
    .map(p => `- [出現回数: ${p.count || 1}] ${p.degrees.join(' → ')}`)
    .join('\n');

  const microRhythm = rhythmPatterns
    .filter(p => p.source === 'original')
    .slice(0, 5)
    .map(p => `- [出現回数: ${p.count || 1}] ${p.description || 'パターン'}`)
    .join('\n');

  // マークダウン文字列の生成（Template Literals）
  return `
# 楽曲分析データと処方箋作成依頼

以下は、私が制作したメロディ（自作曲）と、目標とするリファレンス曲（理想）の分析データです。

## 1. 【基本情報】
- テンポ: 120 BPM前後（ミッドテンポ）
- ジャンル: ポップス / エレクトロニカベース

## 2. 【マクロ分析データ】（スコア比較）
レーダーチャート（0〜100）による特徴比較です。
${macroText}

## 3. 【ミクロ分析データ】（自作曲の抽出フレーズ・手癖）
▽ ピッチパターン（階名推移）の傾向
${microPitch || 'データなし'}

▽ リズムパターンの傾向
${microRhythm || 'データなし'}

## 4. 【システムプロンプト】（あなたの役割と指示）
あなたはプロフェッショナルな音楽プロデューサー兼・作編曲家です。
上記の「マクロ分析データ（自作曲と参考曲のスコア差）」と「ミクロ分析データ（具体的な手癖）」を紐づけて解釈し、自作曲を理想のクオリティに近づけるための具体的な処方箋を提示してください。

[厳守事項]
1. 単純に数値を読み上げるだけの分析（例：「跳躍進行が少ないですね」など）は禁止します。
2. 「なぜそのスコア差が生まれているのか」「手癖がどう影響しているのか」を音楽理論に基づいて考察してください。
3. 私がDAW（作曲ソフト）ですぐに実践できる具体的なアクションを3つ提案してください。
   （例：「〇小節目の2拍目に休符を入れてシンコペーションを強調する」「メジャー3度の音を避けて四六抜きにする」など、具体的に）
`.trim();
}
